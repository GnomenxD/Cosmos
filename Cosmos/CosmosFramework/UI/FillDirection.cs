
namespace CosmosFramework.UI
{
	public enum FillDirection
	{
		/// <summary>
		/// The image does not use filling.
		/// </summary>
		None,
		/// <summary>
		/// Fills the image from left to right.
		/// </summary>
		Left,
		/// <summary>
		/// Fills the image from right to left.
		/// </summary>
		Right,
		/// <summary>
		/// Fills the image from top to bottom.
		/// </summary>
		Top,
		/// <summary>
		/// Fills the image from bottom to top.
		/// </summary>
		Bottom,
	}
}